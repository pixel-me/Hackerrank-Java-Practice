import java.util.*;

public class Java_practice_3 {

    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);
        int a = scan.nextInt();
        System.out.println(a);
        a = scan.nextInt();
        System.out.println(a);
        a = scan.nextInt();
        System.out.println(a);
        scan.close();
    }
}
